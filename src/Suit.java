/**
 * Enum used to state the suit of the card. Either hearts, diamonds, spades, or clubs.
 */
public enum Suit {

    /**
     * If the card is a Heart.
     */
    Hearts,

    /**
     * If the card is a Diamond.
     */
    Diamonds,

    /**
     * If the card is a Spade.
     */
    Spades,

    /**
     * If the card is a Club.
     */
    Clubs
}
